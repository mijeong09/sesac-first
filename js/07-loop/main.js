// for(초기화문; 조건식; 증감문)
for (let i = 0; i < 5; i++) {
  console.log(`${i + 1}번 실행`);
}

// 반복문이 있는 코드
for (let number1 = 0; number1 < 3; number1++) {
  console.log(number1);
}

// 숫자를 0부터 5까지 출력
for (let i = 0; i < 6; i++) {
  console.log(i);
}

